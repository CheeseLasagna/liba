char	*ft_strrchr(const char *s, int c)
{
	int len;

	len = 0;
	while (*s != 0)
	{
		len++;
		s++;
	}
	while (*s != c)
	{
		if (len == 0)
			return (0);
		len--;
		s--;
	}
	return ((char*)s);
}
